<h3>Методы печати</h3>
<ol>
	<li><a href="https://eklektika.ru/shelkografiya/">Шелкография.</a>
	Нанесение логотипа выполняется экологически чистыми и безопасными красками сразу на ткань. Особенность — использование до 8 красок и печать по трафарету. Для него требуется долгая подготовительная работа и настройка оборудования, поэтому вариант выгоден при оптовых тиражах от 50 штук. <br>
	 Только данным способом возможна печать практически по всей поверхности футболки. Учтите: если вы используете один макет логотипа, но хотите, чтобы он отличался по площади на разных изделиях, каждый макет рассматривается как отдельный тираж и рассчитывается отдельно.<br>
 <br>
 </li>
	<li>Прямая цифровая печать. Она дает максимальное качество и идеальную полноцветную цветопередачу на футболках. Это нанесение изображения при помощи цифрового принтера на ткань водными красками. Способ наиболее дорогой, но позволяет изготавливать высококачественные изделия небольшими тиражами за 1 день. </li>
	<li>Термотрансфер. Пленка с напечатанным изображением приклеивается к ткани термопрессом. Такое полноцветное нанесение отличается более низкой ценой при оптовой печати. Износостойкость изображения на футболке будет меньше, чем при брендировании методом шелкографии, так как пленка после стирок в машинке может отслаиваться.<br>
 <br>
 </li>
	<li>Вышивка и шеврон. Эти методы нанесения реже всего используют для персонализации футболок, так как ткань самого изделия достаточно тонкая.<br>
 <br>
 </li>
	<li><a href="https://eklektika.ru/sublimaciya-na-futbolkah-cena/">Сублимация.</a>
	Для нанесения логотипа данным методом футболки должны быть синтетическими и со специальной пропиткой. Для каждой создается бумажный носитель с изображением. А затем при помощи термопресса краски с него переносятся на ткань. Итоговая цена таких изделий прямо пропорциональна количеству, поэтому метод выгоден при минимальных тиражах от 3 шт.</li>
</ol>
