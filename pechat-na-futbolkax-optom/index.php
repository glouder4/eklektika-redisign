<h3>Качество и материал</h3>
<p>
	 Если речь идёт о презентациях, то важно, чтобы люди после мероприятия сохранили о вас память. Если вещи будут приятные и комфортные, то они точно останутся у человека и будут напоминать о вашей компании. Поэтому обратите особое внимание на качество футболок и стоимость. Для печати изображения мы рекомендуем хлопковые изделия. Но есть и футболки с добавлением синтетики, которые хороши для печати методом сублимации при небольшом тираже.
</p>
<h3>Дизайн</h3>
<p>
	 Он играет ключевую роль. Даже если футболка с печатью будет не такая по качеству, как хотелось бы человеку, но суперстильная, он, скорее всего, оставит ее в гардеробе. А вот если качество будет хорошим, а дизайн нет, покупка отправится либо на тряпки, либо на дачу. Продумывая оформление, помните, что человек вряд ли захочет стать вашей ходячей рекламой, поэтому не стоит наносить логотип на всю лицевую сторону. Пусть символика будет изюминкой на сувенире. Можно напечатать изображение на рукаве, внизу или на боку. Это необычно и стильно.
</p>
<h3>Цвет</h3>
<p>
